# Defining variables to use in our examples
x = 10
y = 5

print(f"Variables used for examples: x = {x}, y = {y}\n")

# ---------------------------------------------------------
# Comparison Operators (Evaluating to True or False)
# ---------------------------------------------------------

# 1. Equal to (==)
print(f"Equal to (==)                  : x == y   ->  {x == y}")

# 2. Not equal to (!=)
print(f"Not equal to (!=)              : x != y   ->  {x != y}")

# 3. Greater than (>)
print(f"Greater than (>)               : x > y    ->  {x > y}")

# 4. Less than (<)
print(f"Less than (<)                  : x < y    ->  {x < y}")

# 5. Greater than or equal to (>=)
# Returns True if x is greater than y OR if they are exactly equal.
print(f"Greater than or equal to (>=)  : x >= y   ->  {x >= y}")

# 6. Less than or equal to (<=)
# Returns True if x is less than y OR if they are exactly equal.
print(f"Less than or equal to (<=)     : x <= y   ->  {x <= y}")

